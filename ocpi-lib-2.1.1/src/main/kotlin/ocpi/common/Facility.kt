package ocpi.common

enum class Facility {
    /**
     * A hotel.
     */
    HOTEL,

    /**
     * A restaurant.
     */
    RESTAURANT,

    /**
     * A cafe.
     */
    CAFE,

    /**
     * A mall or shopping center.
     */
    MALL,

    /**
     * A supermarket.
     */
    SUPERMARKET,

    /**
     * Sport facilities: gym, field etc.
     */
    SPORT,

    /**
     * A Recreation area.
     */
    RECREATION_AREA,

    /**
     * Located in, or close to, a park, nature reserve/park etc.
     */
    NATURE,

    /**
     * A museum.
     */
    MUSEUM,

    /**
     * A bus stop.
     */
    BUS_STOP,

    /**
     * A taxi stand.
     */
    TAXI_STAND,

    /**
     * A train station.
     */
    TRAIN_STATION,

    /**
     * An airport.
     */
    AIRPORT,

    /**
     * A carpool parking.
     */
    CARPOOL_PARKING,

    /**
     * A Fuel station.
     */
    FUEL_STATION,

    /**
     * Wifi or other type of internet available.
     */
    WIFI
}
